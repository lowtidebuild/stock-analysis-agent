def _h(lang: str, key: str) -> str:
    """Get section header string for the given language."""
    return SECTION_HEADERS.get(lang, SECTION_HEADERS["en"]).get(key, key)


def set_cell_bg(cell, hex_color: str):
    """Set background fill color of a table cell."""
    tc = cell._tc
    tcPr = tc.get_or_add_tcPr()
    shd = OxmlElement("w:shd")
    shd.set(qn("w:val"), "clear")
    shd.set(qn("w:color"), "auto")
    shd.set(qn("w:fill"), hex_color)
    tcPr.append(shd)


def add_heading(doc, text: str, level: int = 1):
    """Add a styled heading."""
    h = doc.add_heading(text, level=level)
    for run in h.runs:
        run.font.color.rgb = COLOR_TITLE_TEXT if level == 0 else COLOR_SECTION
        run.font.size = Pt({0: 20, 1: 14, 2: 12, 3: 11}.get(level, 11))
    return h


def add_table(doc, headers: list, rows: list):
    """Add a formatted table. Returns the table object."""
    if not rows:
        doc.add_paragraph("[데이터 없음 / No data available]")
        return None

    n_cols = len(headers)
    table = doc.add_table(rows=len(rows) + 1, cols=n_cols)
    table.style = "Table Grid"

    # Header row
    hdr = table.rows[0]
    for i, h in enumerate(headers):
        cell = hdr.cells[i]
        cell.text = str(h)
        set_cell_bg(cell, COLOR_HEADER_BG)
        for run in cell.paragraphs[0].runs:
            run.bold = True
            run.font.size = Pt(9)
        cell.paragraphs[0].alignment = WD_ALIGN_PARAGRAPH.CENTER

    # Data rows
    for r_idx, row_data in enumerate(rows):
        row = table.rows[r_idx + 1]
        for c_idx, val in enumerate(row_data[:n_cols]):
            cell = row.cells[c_idx]
            text = "—" if val is None else str(val)
            cell.text = text
            # Alternate shading
            if r_idx % 2 == 1:
                set_cell_bg(cell, COLOR_ROW_ALT)
            for run in cell.paragraphs[0].runs:
                run.font.size = Pt(9)

    doc.add_paragraph()
    return table


def add_text_block(doc, text, split_paras=True):
    """Add a text block, optionally splitting on double newlines."""
    if not text:
        return
    if isinstance(text, list):
        for item in text:
            add_text_block(doc, item, split_paras)
        return
    text = str(text).strip()
    if not text:
        return
    if split_paras:
        for para in text.split("\n\n"):
            p = para.strip()
            if p:
                doc.add_paragraph(p)
    else:
        doc.add_paragraph(text)


def dict_table(doc, data_dict: dict, key_label="Item", val_label="Value"):
    """Render a simple dict as a 2-column table."""
    rows = [[k.replace("_", " ").title(), v] for k, v in data_dict.items() if v is not None]
    add_table(doc, [key_label, val_label], rows)


def rows_from_list_of_dicts(records):
    """Convert list-of-dicts to (headers, rows) tuple."""
    if not records or not isinstance(records[0], dict):
        return [], []
    headers = [k.replace("_", " ").title() for k in records[0].keys()]
    rows = [[str(rec.get(k, "—") or "—") for k in records[0].keys()] for rec in records]
    return headers, rows


