def generate_docx(data: dict, output_path: str) -> str:
    lang = data.get("output_language", "en").lower()
    if lang not in ("ko", "en"):
        lang = "en"
    L = SECTION_HEADERS[lang]

    doc = Document()

    # Page margins
    sec = doc.sections[0]
    sec.page_width  = Inches(8.5)
    sec.page_height = Inches(11)
    for attr in ("left_margin", "right_margin", "top_margin", "bottom_margin"):
        setattr(sec, attr, Inches(1))

    ticker       = data.get("ticker", "N/A")
    company_name = data.get("company_name", ticker)
    analysis_date = data.get("analysis_date", datetime.now().strftime("%Y-%m-%d"))
    price        = data.get("price_at_analysis", "—")
    currency     = data.get("currency", "USD")
    rr_score     = data.get("rr_score", "—")
    verdict      = data.get("verdict", "—")
    data_mode    = data.get("data_mode", "standard")
    sections     = data.get("sections", {})

    cur_sym = "₩" if currency == "KRW" else "$"

    # -----------------------------------------------------------------------
    # TITLE BLOCK
    # -----------------------------------------------------------------------
    title = doc.add_heading("Investment Memo", 0)
    title.alignment = WD_ALIGN_PARAGRAPH.CENTER

    sub = doc.add_paragraph()
    sub.alignment = WD_ALIGN_PARAGRAPH.CENTER
    r = sub.add_run(f"{company_name}  ({ticker})")
    r.bold = True
    r.font.size = Pt(18)
    r.font.color.rgb = COLOR_TITLE_TEXT

    meta = doc.add_paragraph()
    meta.alignment = WD_ALIGN_PARAGRAPH.CENTER
    price_fmt = (
        f"{cur_sym}{price:,.0f}" if currency == "KRW" and isinstance(price, (int, float))
        else f"{cur_sym}{price:,.2f}" if isinstance(price, (int, float))
        else f"{cur_sym}{price}"
    )
    meta.add_run(f"Date: {analysis_date}  |  Price: {price_fmt}  |  Mode: {data_mode.upper()}")

    vp = doc.add_paragraph()
    vp.alignment = WD_ALIGN_PARAGRAPH.CENTER
    vr = vp.add_run(f"{L['verdict_label']}: {verdict}  |  {L['rr_label']}: {rr_score}")
    vr.bold = True
    vr.font.size = Pt(12)

    doc.add_paragraph()
    doc.add_paragraph("─" * 70)
    doc.add_paragraph()

    # -----------------------------------------------------------------------
    # EXECUTIVE SUMMARY
    # -----------------------------------------------------------------------
    add_heading(doc, L["exec"], 1)
    exec_sec = sections.get("executive_summary", "")

    if isinstance(exec_sec, dict):
        add_table(doc, [L["verdict_label"], ""], [
            [L["verdict_label"],  exec_sec.get("verdict", verdict)],
            [L["rr_label"],       str(exec_sec.get("rr_score", rr_score))],
            [L["price_label"],    price_fmt],
            [L["target_label"],   f"{cur_sym}{exec_sec.get('base_target', '—')}"],
            [L["horizon_label"],  exec_sec.get("horizon", "12 months")],
        ])
        add_text_block(doc, exec_sec.get("narrative", ""))
    elif exec_sec:
        add_text_block(doc, exec_sec)
    else:
        # Fall back to top-level fields
        add_table(doc, [L["verdict_label"], ""], [
            [L["verdict_label"],  verdict],
            [L["rr_label"],       str(rr_score)],
            [L["price_label"],    price_fmt],
        ])

    # -----------------------------------------------------------------------
    # SECTION 1 — Business Overview
    # -----------------------------------------------------------------------
    add_heading(doc, L["s1"], 1)
    s1 = sections.get("business_overview", "")
    if isinstance(s1, dict):
        for key, val in s1.items():
            if val:
                p = doc.add_paragraph()
                p.add_run(key.replace("_", " ").title() + ": ").bold = True
                p.add_run(str(val))
    else:
        add_text_block(doc, s1)

    # -----------------------------------------------------------------------
    # SECTION 2 — Financial Performance
    # -----------------------------------------------------------------------
    add_heading(doc, L["s2"], 1)
    s2 = sections.get("financial_performance", "")

    if isinstance(s2, dict):
        add_text_block(doc, s2.get("narrative", ""))

        for table_key, heading_key in [
            ("revenue_table",    "s2_rev"),
            ("margin_table",     "s2_margin"),
            ("cash_flow_table",  "s2_cf"),
            ("balance_sheet",    "s2_bs"),
        ]:
            records = s2.get(table_key)
            if records and isinstance(records, list):
                add_heading(doc, L[heading_key], 2)
                headers, rows = rows_from_list_of_dicts(records)
                if headers:
                    add_table(doc, headers, rows)

        add_text_block(doc, s2.get("fcf_note", ""))
    else:
        add_text_block(doc, s2)

    # -----------------------------------------------------------------------
    # SECTION 3 — Valuation Analysis
    # -----------------------------------------------------------------------
    add_heading(doc, L["s3"], 1)
    s3 = sections.get("valuation_analysis", "")

    if isinstance(s3, dict):
        add_text_block(doc, s3.get("narrative", ""))

        vt = s3.get("valuation_table")
        if vt and isinstance(vt, list):
            add_heading(doc, L["s3_core"], 2)
            headers, rows = rows_from_list_of_dicts(vt)
            if headers:
                add_table(doc, headers, rows)

        sotp = s3.get("sotp_table")
        if sotp and isinstance(sotp, list):
            add_heading(doc, L["s3_sotp"], 2)
            headers, rows = rows_from_list_of_dicts(sotp)
            if headers:
                add_table(doc, headers, rows)
    elif s3:
        add_text_block(doc, s3)
    else:
        # Fall back: render key_metrics table from top level
        key_metrics = data.get("key_metrics", {})
        if key_metrics:
            rows = []
            for metric, info in key_metrics.items():
                if isinstance(info, dict):
                    val = info.get("value")
                    rows.append([
                        metric.replace("_", " ").title(),
                        "—" if val is None else str(val),
                        info.get("grade", "—"),
                        info.get("tag", ""),
                    ])
            if rows:
                add_table(doc, ["Metric", "Value", "Grade", "Tag"], rows)

    # -----------------------------------------------------------------------
    # SECTION 4 — 5-Question Variant View
    # -----------------------------------------------------------------------
    add_heading(doc, L["s4"], 1)
    vv = sections.get("variant_view", {})

    for q_key, heading_key in [("q1", "q1"), ("q2", "q2"), ("q3", "q3"), ("q4", "q4"), ("q5", "q5")]:
        add_heading(doc, L[heading_key], 2)
        # Accept both flat keys (variant_view_q1) and nested (variant_view.q1)
        content = sections.get(f"variant_view_{q_key}", vv.get(q_key, ""))

        if q_key == "q2":
            # Check for structured catalyst table
            catalysts = sections.get("variant_view_q2_catalysts", vv.get("q2_catalysts"))
            if catalysts and isinstance(catalysts, list) and isinstance(catalysts[0], dict):
                headers, rows = rows_from_list_of_dicts(catalysts)
                add_table(doc, headers, rows)
        if q_key == "q5":
            conditions = sections.get("variant_view_q5_conditions", vv.get("q5_conditions"))
            if conditions and isinstance(conditions, list):
                for cond in conditions:
                    doc.add_paragraph(f"• {cond}" if isinstance(cond, str) else str(cond),
                                      style="List Bullet")

        add_text_block(doc, content)

    # -----------------------------------------------------------------------
    # SECTION 5 — Precision Risk Analysis
    # -----------------------------------------------------------------------
    add_heading(doc, L["s5"], 1)
    risks = sections.get("precision_risks", [])

    if risks and isinstance(risks, list):
        if isinstance(risks[0], dict):
            # Use first record's keys as headers if standard; otherwise use fixed headers
            if "risk" in risks[0] or "name" in risks[0]:
                fixed_headers = ["Risk", "Mechanism", "EBITDA Impact", "Probability", "Mitigation"]
                risk_rows = [
                    [
                        r.get("risk", r.get("name", "—")),
                        r.get("mechanism", "—"),
                        r.get("ebitda_impact", r.get("impact", "—")),
                        r.get("probability", "—"),
                        r.get("mitigation", "—"),
                    ]
                    for r in risks
                ]
                add_table(doc, fixed_headers, risk_rows)
            else:
                headers, rows = rows_from_list_of_dicts(risks)
                add_table(doc, headers, rows)
        else:
            for r in risks:
                doc.add_paragraph(str(r))

    macro = sections.get("macro_risk", "")
    if macro:
        add_heading(doc, L["s5_macro"], 2)
        add_text_block(doc, macro)

    # -----------------------------------------------------------------------
    # SECTION 6 — Investment Scenarios
    # -----------------------------------------------------------------------
    add_heading(doc, L["s6"], 1)
    scenarios = data.get("scenarios", {})

    if scenarios:
        bull = scenarios.get("bull", {})
        base = scenarios.get("base", {})
        bear = scenarios.get("bear", {})

        bull_prob = int((bull.get("probability") or 0) * 100)
        base_prob = int((base.get("probability") or 0) * 100)
        bear_prob = int((bear.get("probability") or 0) * 100)

        def fmt_target(t):
            if t is None:
                return "—"
            return f"{cur_sym}{t:,.0f}" if currency == "KRW" else f"{cur_sym}{t}"

        def fmt_ret(r):
            if r is None:
                return "—"
            sign = "+" if float(r) >= 0 else ""
            return f"{sign}{r}%"

        add_table(doc, ["", L["bull"], L["base"], L["bear"]], [
            [L["prob_row"],       f"{bull_prob}%",             f"{base_prob}%",             f"{bear_prob}%"],
            [L["target_row"],     fmt_target(bull.get("target")),  fmt_target(base.get("target")),  fmt_target(bear.get("target"))],
            [L["return_row"],     fmt_ret(bull.get("return_pct")),  fmt_ret(base.get("return_pct")),  fmt_ret(bear.get("return_pct"))],
            [L["assumption_row"], bull.get("key_assumption", "—"), base.get("key_assumption", "—"), bear.get("key_assumption", "—")],
        ])

        # R/R formula
        bull_ret = bull.get("return_pct", 0) or 0
        base_ret = base.get("return_pct", 0) or 0
        bear_ret = bear.get("return_pct", 0) or 0

        rr_p = doc.add_paragraph()
        rr_p.add_run(f"{L['rr_label']}: {rr_score}").bold = True
        rr_p.add_run(
            f"\nR/R = ({bull_ret}% × {bull_prob}% + {base_ret}% × {base_prob}%) "
            f"/ |{bear_ret}% × {bear_prob}%| = {rr_score}"
        )

        # Scenario narratives
        inv_sec = sections.get("investment_scenarios", {})
        if isinstance(inv_sec, dict):
            narratives = inv_sec.get("narratives", {})
            for case_key, label in [("bull", L["bull"]), ("base", L["base"]), ("bear", L["bear"])]:
                if narratives.get(case_key):
                    p = doc.add_paragraph()
                    p.add_run(f"{label}: ").bold = True
                    p.add_run(narratives[case_key])
        elif isinstance(inv_sec, str) and inv_sec:
            add_text_block(doc, inv_sec)

    # -----------------------------------------------------------------------
    # SECTION 7 — Peer Comparison
    # -----------------------------------------------------------------------
    add_heading(doc, L["s7"], 1)
    peers = sections.get("peer_comparison", [])

    if isinstance(peers, list) and peers:
        headers, rows = rows_from_list_of_dicts(peers) if isinstance(peers[0], dict) else (["Peer"], [[str(p)] for p in peers])
        add_table(doc, headers, rows)

    peer_narrative = sections.get("peer_comparison_narrative", "")
    if peer_narrative:
        add_text_block(doc, peer_narrative)

    # -----------------------------------------------------------------------
    # SECTION 8 — Management & Corporate Governance
    # -----------------------------------------------------------------------
    add_heading(doc, L["s8"], 1)
    s8 = sections.get("management_governance", "")

    if isinstance(s8, dict):
        for key, val in s8.items():
            if val:
                p = doc.add_paragraph()
                p.add_run(key.replace("_", " ").title() + ": ").bold = True
                p.add_run(str(val))
    else:
        add_text_block(doc, s8)

    # -----------------------------------------------------------------------
    # SECTION 9 — Quality of Earnings
    # -----------------------------------------------------------------------
    add_heading(doc, L["s9"], 1)
    qoe = sections.get("quality_of_earnings", sections.get("qoe_summary", ""))

    if isinstance(qoe, dict):
        bridge = qoe.get("ebitda_bridge")
        if bridge and isinstance(bridge, list):
            add_heading(doc, L["s9_bridge"], 2)
            headers, rows = rows_from_list_of_dicts(bridge)
            if headers:
                add_table(doc, headers, rows)

        add_text_block(doc, qoe.get("narrative", ""))

        fcf = qoe.get("fcf_conversion")
        if fcf:
            p = doc.add_paragraph()
            p.add_run("FCF Conversion: ").bold = True
            p.add_run(str(fcf))

        sustainability = qoe.get("earnings_sustainability", "")
        if sustainability:
            doc.add_paragraph(sustainability)
    else:
        add_text_block(doc, qoe)

    # -----------------------------------------------------------------------
    # SECTION 10 — What Would Make Me Wrong
    # -----------------------------------------------------------------------
    add_heading(doc, L["s10"], 1)
    wrong = sections.get("what_would_make_me_wrong", "")

    if isinstance(wrong, dict):
        assumptions = wrong.get("assumptions", [])
        for i, assumption in enumerate(assumptions, 1):
            if isinstance(assumption, dict):
                add_heading(doc, f"Assumption {i}: {assumption.get('assumption', '')}", 2)
                for field, label in [
                    ("if_wrong",           "If wrong"),
                    ("monitoring_indicator", "Monitoring indicator"),
                    ("probability",         "Probability of being wrong"),
                ]:
                    if assumption.get(field):
                        p = doc.add_paragraph()
                        p.add_run(label + ": ").bold = True
                        p.add_run(str(assumption[field]))
            else:
                doc.add_paragraph(str(assumption))
        pre_mortem = wrong.get("pre_mortem", "")
        if pre_mortem:
            p = doc.add_paragraph()
            p.add_run("Pre-Mortem: ").bold = True
            p.add_run(pre_mortem)

    elif isinstance(wrong, list):
        for i, item in enumerate(wrong, 1):
            if isinstance(item, dict):
                add_heading(doc, f"Assumption {i}: {item.get('assumption', '')}", 2)
                for field, label in [
                    ("if_wrong",           "If wrong"),
                    ("monitoring_indicator", "Monitoring indicator"),
                    ("probability",         "Probability of being wrong"),
                ]:
                    if item.get(field):
                        p = doc.add_paragraph()
                        p.add_run(label + ": ").bold = True
                        p.add_run(str(item[field]))
            else:
                doc.add_paragraph(str(item))
        pre_mortem = sections.get("pre_mortem", "")
        if pre_mortem:
            p = doc.add_paragraph()
            p.add_run("Pre-Mortem: ").bold = True
            p.add_run(pre_mortem)
    else:
        add_text_block(doc, wrong)

    # -----------------------------------------------------------------------
    # APPENDIX — Data Sources & Confidence
    # -----------------------------------------------------------------------
    add_heading(doc, L["appendix"], 1)
    data_sources = sections.get("data_sources", data.get("data_sources", []))

    if data_sources and isinstance(data_sources, list) and isinstance(data_sources[0], dict):
        headers, rows = rows_from_list_of_dicts(data_sources)
        add_table(doc, headers, rows)

    dq = data.get("data_quality_used", {})
    if dq:
        p = doc.add_paragraph()
        p.add_run("Data Quality Summary: ").bold = True
        p.add_run(
            f"Grade A: {dq.get('grade_A_count', 0)} | "
            f"Grade B: {dq.get('grade_B_count', 0)} | "
            f"Grade C: {dq.get('grade_C_count', 0)} | "
            f"Grade D (excluded): {dq.get('grade_D_count', 0)} | "
            f"Overall: {dq.get('overall_grade', '—')}"
        )
        excl = dq.get("exclusions", [])
        if excl:
            p2 = doc.add_paragraph()
            p2.add_run("Exclusions: ").bold = True
            for e in excl:
                if isinstance(e, dict):
                    p2.add_run(f"\n  • {e.get('metric', '—')}: {e.get('reason', '—')}")

    mode_str = (
        "Enhanced (Financial Datasets MCP active)"
        if data_mode == "enhanced"
        else "Standard (Web-only)"
    )
    doc.add_paragraph(f"Data Mode: {mode_str}")

    # -----------------------------------------------------------------------
    # DISCLAIMER
    # -----------------------------------------------------------------------
    doc.add_paragraph()
    doc.add_paragraph("─" * 70)

    disc_p = doc.add_paragraph()
    disc_run = disc_p.add_run(L["disclaimer"])
    disc_run.font.size = Pt(8)
    disc_run.font.color.rgb = COLOR_DISCLAIMER

    gen_p = doc.add_paragraph()
    gen_run = gen_p.add_run(
        f"Generated: {datetime.now().strftime('%Y-%m-%d %H:%M')} UTC  |  "
        f"AI Research Assistant  |  Data Mode: {mode_str}"
    )
    gen_run.font.size = Pt(8)
    gen_run.font.color.rgb = COLOR_GENERATED

    # -----------------------------------------------------------------------
    # SAVE
    # -----------------------------------------------------------------------
    os.makedirs(os.path.dirname(os.path.abspath(output_path)), exist_ok=True)
    doc.save(output_path)
    print(f"DOCX saved: {output_path}")
    return output_path


